/* Continuação dos operadores combinados 
* Operadores de atribuição combinados:
* a = a + 10;	equivalente a:  a += 10; <- significa a toma o valor de a + 10. É simplesmente outra forma de escrever a mesma coisa.
* a = a + 1;	equivalente a:  a += 1; OU a++ OU ++a <- atenção que o ++ só serve para incrementar uma unidade.
* 
* O mesmo se aplica ao decremento:
* b = b - 10;	equivalente a: a -= 10; <- significa que b toma o valor de b - 10.
* b = b - 1;	equivalente a: b -= 1; OU b-- OU --b <- na mesma só para uma unidade.
* 
* Multiplicação:
* c = c * 10;	equivalente a: c *= 10; <- significa que c toma o valor de c a multiplicar por 10.
* Aqui já não há c** nem nada disso. Já não funciona.
* 
* Divisão:
* d = d / 10;	equivalente a: d /= 10; <- significa que d toma o valor de d a dividir por 10.
* Aqui também não há d// nem nada disso.
* 
* Resto da divisão inteira: (que no pseudo era MOD):
* e = e % 2;	equivalente a: e %= 2; <- significa que e toma o valor da divisão inteira de e.
*/

